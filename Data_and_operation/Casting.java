package Data_and_operation;

public class Casting {
    public static void main(String[] args) {
        // 데이터 타입의 변환 (Casting)

        int a = (int) 1.1;
        System.out.println(a);

        // 1 to String
        String k = Integer.toString(1);
        System.out.println(k);
        System.out.println(k.getClass());

    }
}   
